package controlstatement;

public class WhileLoopDemo 
{
    public static void WLD()
    {
        int i = 0;
        while(i<5)
        {
            System.out.println(i + " Bangladesh");
            i++;
        }
        
        System.out.println("\n");
        int nb = 2;
        while(nb<20)
        {
            System.out.println(nb);
            nb = nb + 2;
        }
    }
}
