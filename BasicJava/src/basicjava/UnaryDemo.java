package basicjava;

public class UnaryDemo 
{
    public static void UD()
    {
        int x = 10;
        int result = +x; //There are positive value 
        System.out.println("Value : " +result);
        
        result = -x; //There are negative value
        System.out.println("Value : " +result + "\n\n");
    }
}
