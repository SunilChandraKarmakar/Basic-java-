package basicjava;

public class AssigmentOperator 
{
    public static void AllAssOp()
    {
        int x = 3,  y = 2;
        
        x += y; //x = x + y
        int result = x;
        System.out.println("x = " +result);
        
        x -= y; //x = x - y
        result = x;
        System.out.println("x = " +result);
        
        x *= y; //x = x * y
        result = x;
        System.out.println("x = " +result);
        
        x /= y; //x = x / y
        result = x;
        System.out.println("x = " +result);
        
        x %= y; //x = x % y
        result = x;
        System.out.println("x = " +result + "\n\n");
    }
}
